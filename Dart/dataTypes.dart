// Data Types in Dart
void main() {
  // 1. int & double
  int num = 3;
  double dec = 3.6;
  print("Numbers : $num & $dec");

  // 2.String
  String name = "Arshad";
  print("String : $name");

  // 3.Boolean
  bool option = true;
  print("$option");

  // 4. List

  List l1 = ["a", 5, "b"];
  print("$l1");

  // 5.Map
  Map dict = {"FName": "Arshad", "Lname": "Khan", "Number": 9082463374};
  print("$dict");
}
