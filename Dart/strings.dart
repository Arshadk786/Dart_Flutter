void main() {
  // Strings

  String s1 = 'aaabdh';
  String s2 = "afFGaefc";
  String s3 = """  abdcafcqah """;
  String s4 = '''ab/dca/cah''';
  print(s3);

  // String Methods
  // 1.length
  print(s1.length);

  // 2.isEmpty
  print(s2.isEmpty);

  // 3.toUpperCase()
  print(s4.toUpperCase());

  // 4.toLowerCaes()
  print(s2.toLowerCase());

  // 5.trim()
  print(s3.trim());

  // 6.substring()
  print(s3.substring(5, 8));

  // 7.split()
  print(s4.split("/"));
}
